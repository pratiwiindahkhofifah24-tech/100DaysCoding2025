public class Codingday25 {
    public static void main(String[] args) {
        int x = 5;
        
        System.out.println("Awal x = " + x);
        
        x++;  // increment, x jadi 6
        System.out.println("Setelah increment x = " + x);
        
        x--;  // decrement, x balik ke 5
        System.out.println("Setelah decrement x = " + x);
    }
}
